export const translate = (language, key, vars = {}) => {
  const lang = TEXTS[language] ? language : 'he';
  const template = TEXTS[lang][key] || TEXTS.he[key] || key;

  return Object.entries(vars).reduce((result, [name, value]) => {
    return result.replace(`{${name}}`, String(value));
  }, template);
};

export const isRtlLanguage = (language) => language === 'he';
